package com.tns.Looping_Iteration;

public class AssignmentQuestion3_Pattern {
	  
	   public static void main(String[] args)  
	   {  
	   int i,j, n=6;  
	   System.out.println("The Pattern Is :");  
	   for(i=1;i<n;i++)  
	   {  
	   for(j=1;j<=i;j++)  
	   {  
	       System.out.print(" @");  
	   }  
	       System.out.println("");  
	   }  
     }
 }